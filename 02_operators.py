### Operadores Aritméticos

print(2 + 3) # Suma
print(2 - 3) # Resta
print(2 * 3) # Multiplicación
print(2 / 3) # División
print(10 % 3) # Módulo (Residuo de una división)
print(2 ** 3) # Potencia
print(10 // 3) # División Entera

print("Hola " + "Mundo") # Concatenación
print("Hola " * 3) # Repetición

### Operadores de Comparación

print("Hola" == "Hola") # Igualdad
print(2 != 3) # Diferencia
print(2 < 3) # Menor que
print(2 > 3) # Mayor que
print(2 <= 3) # Menor o igual que
print(2 >= 3) # Mayor o igual que

### Operadores Lógicos

print(2 < 3 and "Hola" == "Hola") # True && True = True
print(2 < 3 or "Hola" != "Hola") # True || False = False
print(not (2 < 3)) # !=True = False

