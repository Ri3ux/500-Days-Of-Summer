# Métodos de las listas
append()      # Agrega un elemento al final de la lista
extend()      # Agrega los elementos de una lista a otra lista
insert()      # Agrega un elemento en una posición específica
remove()      # Elimina la primera aparición de un elemento específico
pop()         # Elimina y devuelve el elemento en una posición específica
clear()       # Elimina todos los elementos de la lista
sort()        # Ordena los elementos de la lista
reverse()     # Invierte el orden de los elementos de la lista
index()       # Devuelve el índice de la primera aparición de un elemento específico
count()       # Devuelve el número de veces que un elemento específico aparece en la lista
copy()        # Devuelve una copia superficial de la lista

# Funciones útiles para listas
sorted()      # Devuelve una nueva lista ordenada sin modificar la original